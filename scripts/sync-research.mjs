#!/usr/bin/env node
/**
 * sync-research.mjs
 * Scans ~/Documents/Research/*.md, parses metadata, copies files to
 * public/research/papers/, optionally generates TTS audio, and writes
 * public/research/manifest.json.
 *
 * Usage:
 *   node scripts/sync-research.mjs              # with audio
 *   node scripts/sync-research.mjs --no-audio   # skip TTS
 */

import fs from 'fs';
import path from 'path';
import os from 'os';
import { execSync } from 'child_process';

// ---------------------------------------------------------------------------
// Config
// ---------------------------------------------------------------------------
const RESEARCH_DIR = path.join(os.homedir(), 'Documents', 'Research');
const OUT_PAPERS_DIR = path.join(process.cwd(), 'public', 'research', 'papers');
const OUT_AUDIO_DIR = path.join(process.cwd(), 'public', 'research', 'audio');
const MANIFEST_PATH = path.join(process.cwd(), 'public', 'research', 'manifest.json');
const NO_AUDIO = process.argv.includes('--no-audio');
const WORDS_PER_MINUTE = 200;
const TTS_CHAR_LIMIT = 4096;

// ---------------------------------------------------------------------------
// Category inference
// ---------------------------------------------------------------------------
const CATEGORY_RULES = [
  { patterns: ['anvil-', 'swppp-', 'compliance-', 'stormwater-', 'nyc-metro-stormwater-', 'water-engineering-'], category: 'Project Anvil' },
  { patterns: ['ai-', 'ai_'], category: 'AI Infrastructure' },
  { patterns: ['raspberry-pi-', 'openai-codex-cli-on-raspberry-'], category: 'Hardware' },
  { patterns: ['wedding-', 'dual-ceremony-'], category: 'Planning' },
  { patterns: ['instagram-'], category: 'Growth' },
  { patterns: ['system-decomposition-'], category: 'Systems Engineering' },
  { patterns: ['secure-'], category: 'Security' },
];

function inferCategory(filename) {
  const base = path.basename(filename, '.md').toLowerCase();

  // For auto-generated files starting with a date (e.g. 2026-*)
  // check if any pattern appears anywhere in the filename
  const isAutoGenerated = /^\d{4}-/.test(base);

  for (const { patterns, category } of CATEGORY_RULES) {
    for (const p of patterns) {
      if (isAutoGenerated) {
        if (base.includes(p)) return category;
      } else {
        if (base.startsWith(p)) return category;
      }
    }
  }
  return 'Other';
}

// ---------------------------------------------------------------------------
// Title extraction
// ---------------------------------------------------------------------------
function toTitleCase(str) {
  const minors = new Set(['a', 'an', 'the', 'and', 'but', 'or', 'for', 'nor',
    'on', 'at', 'to', 'by', 'in', 'of', 'up', 'as', 'is', 'it']);
  return str
    .split(' ')
    .map((word, i) => {
      if (i === 0 || !minors.has(word.toLowerCase())) {
        return word.charAt(0).toUpperCase() + word.slice(1).toLowerCase();
      }
      return word.toLowerCase();
    })
    .join(' ');
}

function humanizeFilename(filename) {
  let base = path.basename(filename, '.md');
  // Strip trailing date suffix like -2026-03-17
  base = base.replace(/-\d{4}-\d{2}-\d{2}$/, '');
  // Strip leading date-numbered prefix like 2026-03-26-0002-
  base = base.replace(/^\d{4}-\d{2}-\d{2}-\d+-/, '');
  // Replace hyphens and underscores with spaces
  base = base.replace(/[-_]+/g, ' ');
  return toTitleCase(base.trim());
}

function extractTitle(lines, filename) {
  for (const line of lines) {
    const m = line.match(/^#\s+(.+)/);
    if (m) return m[1].trim();
  }
  return humanizeFilename(filename);
}

// ---------------------------------------------------------------------------
// Description extraction
// ---------------------------------------------------------------------------

// Lines to skip when looking for description
const SKIP_LINE_PATTERNS = [
  /^#{1,6}\s/,                       // any heading
  /^---+\s*$/,                       // hr
  /^(date|prepared for|classification|source)\s*:/i,  // metadata labels
  /^\*\*\w[\w\s]*:\*\*/,             // bold label like **Date:**
  /^\s*$/,                           // blank
  /^[-*]\s/,                         // list items
  /^\|/,                             // tables
  /^>/,                              // blockquotes
  /^```/,                            // code fences
  /^-\s*Queue ID:/i,                 // queue metadata
  /^-\s*Generated:/i,
];

function isSkippableLine(line) {
  return SKIP_LINE_PATTERNS.some(re => re.test(line));
}

function extractDescription(lines) {
  // Collect paragraph text, skipping metadata lines
  let paragraph = [];
  for (const line of lines) {
    if (isSkippableLine(line)) {
      if (paragraph.length > 0) {
        // We found a non-empty paragraph; emit it
        break;
      }
      continue;
    }
    paragraph.push(line.trim());
  }
  const text = paragraph.join(' ').trim();
  if (!text) return '';
  return text.slice(0, 200);
}

// ---------------------------------------------------------------------------
// Date extraction
// ---------------------------------------------------------------------------
function extractDate(filename, stat) {
  const base = path.basename(filename, '.md');
  const m = base.match(/(\d{4}-\d{2}-\d{2})/);
  if (m) return m[1];
  // Fall back to file mtime
  return stat.mtime.toISOString().slice(0, 10);
}

// ---------------------------------------------------------------------------
// Read time
// ---------------------------------------------------------------------------
function readTime(text) {
  const wordCount = text.split(/\s+/).filter(Boolean).length;
  return Math.max(1, Math.ceil(wordCount / WORDS_PER_MINUTE));
}

// ---------------------------------------------------------------------------
// Markdown stripping for TTS
// ---------------------------------------------------------------------------
function stripMarkdown(text) {
  return text
    .replace(/```[\s\S]*?```/g, '')        // code blocks
    .replace(/`[^`]+`/g, '')               // inline code
    .replace(/!\[.*?\]\(.*?\)/g, '')        // images
    .replace(/\[([^\]]+)\]\([^)]+\)/g, '$1') // links -> text
    .replace(/^#{1,6}\s+/gm, '')           // headings
    .replace(/\*\*(.+?)\*\*/g, '$1')       // bold
    .replace(/\*(.+?)\*/g, '$1')           // italic
    .replace(/^[-*]\s+/gm, '')             // list markers
    .replace(/^\|.*\|$/gm, '')             // tables
    .replace(/^---+$/gm, '')               // hr
    .replace(/\n{3,}/g, '\n\n')            // excess newlines
    .trim();
}

// ---------------------------------------------------------------------------
// Split text into TTS-safe chunks (at paragraph boundaries)
// ---------------------------------------------------------------------------
function splitForTTS(text) {
  const paragraphs = text.split(/\n\n+/);
  const chunks = [];
  let current = '';

  for (const para of paragraphs) {
    const segment = para.trim();
    if (!segment) continue;
    if ((current + '\n\n' + segment).length <= TTS_CHAR_LIMIT) {
      current = current ? current + '\n\n' + segment : segment;
    } else {
      if (current) chunks.push(current);
      // If single paragraph exceeds limit, split at sentence boundaries
      if (segment.length > TTS_CHAR_LIMIT) {
        const sentences = segment.match(/[^.!?]+[.!?]+/g) || [segment];
        let sentChunk = '';
        for (const s of sentences) {
          if ((sentChunk + ' ' + s).length <= TTS_CHAR_LIMIT) {
            sentChunk = sentChunk ? sentChunk + ' ' + s : s;
          } else {
            if (sentChunk) chunks.push(sentChunk);
            sentChunk = s;
          }
        }
        if (sentChunk) current = sentChunk;
        else current = '';
      } else {
        current = segment;
      }
    }
  }
  if (current) chunks.push(current);
  return chunks;
}

// ---------------------------------------------------------------------------
// TTS generation via OpenAI API
// ---------------------------------------------------------------------------
async function generateAudio(text, outputPath) {
  const apiKey = process.env.OPENAI_API_KEY;
  if (!apiKey) {
    throw new Error('OPENAI_API_KEY environment variable is required for audio generation');
  }

  const cleaned = stripMarkdown(text);
  const chunks = splitForTTS(cleaned);
  console.log(`  Generating audio: ${chunks.length} chunk(s)...`);

  const buffers = [];

  for (let i = 0; i < chunks.length; i++) {
    const chunk = chunks[i];
    const response = await fetch('https://api.openai.com/v1/audio/speech', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${apiKey}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'tts-1',
        voice: 'alloy',
        input: chunk,
      }),
    });

    if (!response.ok) {
      const err = await response.text();
      throw new Error(`OpenAI TTS error (chunk ${i + 1}): ${err}`);
    }

    const arrayBuffer = await response.arrayBuffer();
    buffers.push(Buffer.from(arrayBuffer));
    process.stdout.write(`    chunk ${i + 1}/${chunks.length} done\n`);
  }

  const combined = Buffer.concat(buffers);
  fs.writeFileSync(outputPath, combined);
  console.log(`  Audio saved: ${outputPath}`);
}

// ---------------------------------------------------------------------------
// Category sort helper: "Other" goes last
// ---------------------------------------------------------------------------
function sortCategories(cats) {
  const sorted = cats.filter(c => c !== 'Other').sort();
  if (cats.includes('Other')) sorted.push('Other');
  return sorted;
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------
async function main() {
  console.log(`Scanning: ${RESEARCH_DIR}`);
  console.log(`Audio generation: ${NO_AUDIO ? 'SKIPPED (--no-audio)' : 'ENABLED'}`);
  console.log('');

  // Check API key early when audio is enabled
  if (!NO_AUDIO && !process.env.OPENAI_API_KEY) {
    console.error('Error: OPENAI_API_KEY environment variable is required for audio generation.');
    console.error('Run with --no-audio to skip, or set OPENAI_API_KEY.');
    process.exit(1);
  }

  // Ensure output dirs exist
  fs.mkdirSync(OUT_PAPERS_DIR, { recursive: true });
  if (!NO_AUDIO) {
    fs.mkdirSync(OUT_AUDIO_DIR, { recursive: true });
  }

  // Read all .md files
  const allFiles = fs.readdirSync(RESEARCH_DIR);
  const mdFiles = allFiles.filter(f => f.endsWith('.md'));
  console.log(`Found ${mdFiles.length} .md files`);

  const papers = [];
  const categorySet = new Set();

  for (const filename of mdFiles) {
    const filePath = path.join(RESEARCH_DIR, filename);
    const stat = fs.statSync(filePath);
    const rawContent = fs.readFileSync(filePath, 'utf8');
    const lines = rawContent.split('\n');

    const slug = path.basename(filename, '.md');
    const title = extractTitle(lines, filename);
    const description = extractDescription(lines);
    const category = inferCategory(filename);
    const date = extractDate(filename, stat);
    const readTimeMinutes = readTime(rawContent);

    // Copy .md to output dir
    const destPath = path.join(OUT_PAPERS_DIR, filename);
    fs.copyFileSync(filePath, destPath);

    // Audio
    const audioFilename = `${slug}.mp3`;
    const audioPath = path.join(OUT_AUDIO_DIR, audioFilename);
    let hasAudio = fs.existsSync(audioPath);

    if (!NO_AUDIO && !hasAudio) {
      console.log(`[audio] ${filename}`);
      try {
        await generateAudio(rawContent, audioPath);
        hasAudio = true;
      } catch (err) {
        console.error(`  Failed: ${err.message}`);
        hasAudio = false;
      }
    }

    papers.push({
      slug,
      title,
      description,
      category,
      date,
      readTimeMinutes,
      hasAudio,
      audioFile: hasAudio ? `audio/${audioFilename}` : null,
    });

    categorySet.add(category);
  }

  // Sort papers by date descending
  papers.sort((a, b) => b.date.localeCompare(a.date));

  const categories = sortCategories([...categorySet]);

  const manifest = {
    generatedAt: new Date().toISOString(),
    papers,
    categories,
  };

  fs.writeFileSync(MANIFEST_PATH, JSON.stringify(manifest, null, 2));
  console.log('');
  console.log(`Manifest written: ${MANIFEST_PATH}`);
  console.log(`Papers: ${papers.length}`);
  console.log(`Categories: ${categories.join(', ')}`);
  console.log('Done.');
}

main().catch(err => {
  console.error('Fatal error:', err.message);
  process.exit(1);
});
